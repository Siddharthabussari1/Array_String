class StringArray 
{
	public static void main(String[] args) 
	{
		String [] a = new String[4];

		a[0]="apple";
		a[1]="ball";
		a[2]="cat";
		a[3]="dog";

		System.out.println(a[3]);
		System.out.println(a[2]);
		System.out.println(a[1]);
		System.out.println(a[0]);
		System.out.println(a.length);
	}
}
